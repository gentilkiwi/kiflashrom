FT_STATUS  NRF24L_Status_Wait_Ready(FT_HANDLE handle, BYTE* pStatus)
{
	FT_STATUS status;
	BYTE Status;

	do
	{
		status = Generic_Read_Status(handle, &Status);
		if (!FT_SUCCESS(status))
		{
			return status;
		}

	} while (Status & NRF24_STATUS_RDYN);

	if (pStatus)
	{
		*pStatus = Status;
	}

	return status;
}

FT_STATUS NRF24L_Write_Enable_Confirmed(FT_HANDLE handle)
{
	FT_STATUS status;
	BYTE Status;

	status = Generic_Write_Enable(handle);
	if (!FT_SUCCESS(status))
	{
		return status;
	}

	status = Generic_Read_Status(handle, &Status);
	if (!FT_SUCCESS(status))
	{
		return status;
	}

	if (!(Status & NRF24_STATUS_WEN))
	{
		status = FT_OTHER_ERROR;
	}

	return status;
}

