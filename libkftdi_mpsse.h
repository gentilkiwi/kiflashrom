#define KFTDI_MPSSE_DATA_SHIFTING_OUT_CLK_EDGE_NEG	(KFTDI_MPSSE_DATA_SHIFTING_CLK_EDGE_NEG << 0)
#define KFTDI_MPSSE_DATA_SHIFTING_BYTE_MODE			(0 << 1)
#define KFTDI_MPSSE_DATA_SHIFTING_BIT_MODE			(1 << 1)
