#define KFTDI_MPSSE_GPIO_HIGH_BYTE		(1 << 1)

#define KFTDI_MPSSE_CMD_GPIO_WRITE_AD			(0x80 | KFTDI_MPSSE_GPIO_LOW_BYTE | KFTDI_MPSSE_GPIO_WRITE)		// 0x80
