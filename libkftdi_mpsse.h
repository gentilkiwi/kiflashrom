#define KFTDI_MPSSE_DATA_SHIFTING_CS_NOWRITE		(0 << 6)
#define KFTDI_MPSSE_DATA_SHIFTING_CS_WRITE			(1 << 6)


#define KFTDI_MPSSE_GPIO_WRITE			(0 << 0)
